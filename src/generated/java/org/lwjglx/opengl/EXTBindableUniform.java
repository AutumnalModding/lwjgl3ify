package org.lwjglx.opengl;

public class EXTBindableUniform {
    public static int glGetUniformBufferSizeEXT(int arg0, int arg1) {
        return org.lwjgl.opengl.EXTBindableUniform.glGetUniformBufferSizeEXT(arg0, arg1);
    }

    public static long glGetUniformOffsetEXT(int arg0, int arg1) {
        return org.lwjgl.opengl.EXTBindableUniform.glGetUniformOffsetEXT(arg0, arg1);
    }

    public static void glUniformBufferEXT(int arg0, int arg1, int arg2) {
        org.lwjgl.opengl.EXTBindableUniform.glUniformBufferEXT(arg0, arg1, arg2);
    }


}
